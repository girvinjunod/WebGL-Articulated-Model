function load(file, callback) {
  if (file.type && file.type.indexOf("json") === -1) {
    console.log("File is not an JSON.", file.type, file);
    return;
  }
  const reader = new FileReader();
  reader.addEventListener("load", (event) => {
    let data = JSON.parse(
      atob(
        event.target.result
          .toString()
          .match(/(?<=base64,).*/)
          .toString()
      )
    );
    callback(data);
  });
  reader.readAsDataURL(file);
}

